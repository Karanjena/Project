package Comm;

public class C {
	public static void main(String[] args) {
		byte b=28;
		System.out.println(b);
		int i=b;
		System.out.println(i);
		System.out.println("************");
		
		double d=68.4879;
		System.out.println(d);
		char c=(char)d;
		System.out.println(c);
		System.out.println("************");
		
		float f=48f;
		System.out.println(f);
		int a=(int)f;
		System.out.println(a);
		System.out.println("************");
		
		short s=90;
		System.out.println(s);
		char e=(char)s;
		System.out.println(e);
	}
}
